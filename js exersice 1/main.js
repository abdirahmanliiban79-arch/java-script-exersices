let x = 10;
let y = "20";

// equal to
console.log(x == y)
// not equal to
console.log(x != y)

// strictly equal to
console.log(x === y)

// strictly not equal to
console.log(x !== y)

// greater than
console.log(x > y)

// less than
console.log(x < y)

// greater than or equal to
console.log(x >= y)

// less than or equal to
console.log(x <= y)