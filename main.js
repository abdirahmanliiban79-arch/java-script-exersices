let x = 7;
let y = 3;
console.log(x + y)
console.log(x - y)
console.log(x * y)
console.log(x / y)
console.log(x % y)
// post increment of x
console.log(x++)
console.log(x)
// pre-increment of x
console.log(++x)

// post increment of y
console.log(y++)
console.log(y)
// pre-increment of y
console.log(++y)
